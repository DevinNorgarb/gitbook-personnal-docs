#!/usr/bin/env node
/**
 * Parse GitBook SUMMARY.md → VitePress default-theme sidebar.
 * Run from repo root: node scripts/summary-to-vitepress-sidebar.mjs
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const SUMMARY = path.join(ROOT, "SUMMARY.md");
const OUT = path.join(ROOT, ".vitepress", "sidebar.generated.mjs");

/** path/to/file.md → VitePress link */
function mdToLink(filePath) {
  const normalized = filePath.replace(/\\/g, "/").trim();
  if (!/\.md$/i.test(normalized)) {
    return "/" + normalized.replace(/^\//, "").replace(/\/$/, "");
  }
  const base = normalized.slice(0, -3);
  const segments = base.split("/");
  const last = segments[segments.length - 1] || "";
  if (/^readme$/i.test(last)) {
    const dir = segments.slice(0, -1).join("/");
    // Root README.md is synced to index.md and served at `/`.
    if (!dir) return "/";
    // Section README pages are emitted as `/path/README.html` with cleanUrls=false.
    // Linking to `/path/README` avoids 404s on `/path/`.
    return "/" + dir + "/README";
  }
  return "/" + base;
}

function indentLevel(line) {
  const m = line.match(/^(\s*)-/);
  if (!m) return null;
  const spaces = m[1].length;
  return Math.floor(spaces / 2);
}

function parseListItem(line) {
  const trimmed = line.trim();
  const m = trimmed.match(/^-\s*\[([^\]]*)\]\(([^)]+)\)\s*$/);
  if (!m) return null;
  return { text: m[1].trim(), file: m[2].trim() };
}

function main() {
  const raw = fs.readFileSync(SUMMARY, "utf8");
  const lines = raw.split(/\r?\n/);

  /** @type {{ text: string, items: any[] }[]} */
  const groups = [];
  let currentGroup = { text: "Start", items: [] };
  groups.push(currentGroup);

  /** stack of { item, level } where item has .items array */
  const stack = [];

  for (const line of lines) {
    if (/^##\s+/.test(line)) {
      const title = line.replace(/^##\s+/, "").trim();
      currentGroup = { text: title, items: [] };
      groups.push(currentGroup);
      stack.length = 0;
      continue;
    }

    const level = indentLevel(line);
    if (level === null) continue;

    const parsed = parseListItem(line);
    if (!parsed) continue;

    const link = mdToLink(parsed.file);
    const node = { text: parsed.text, link };

    while (stack.length > 0 && stack[stack.length - 1].level >= level) {
      stack.pop();
    }

    if (stack.length === 0) {
      currentGroup.items.push(node);
      stack.push({ item: node, level });
    } else {
      const parent = stack[stack.length - 1].item;
      if (!parent.items) parent.items = [];
      parent.items.push(node);
      stack.push({ item: node, level });
    }
  }

  /** Dedupe link targets inside tree (first wins) */
  const seen = new Set();
  function dedupe(items) {
    if (!items) return [];
    const out = [];
    for (const it of items) {
      if (it.items) {
        it.items = dedupe(it.items);
        out.push(it);
        continue;
      }
      if (it.link && seen.has(it.link)) continue;
      if (it.link) seen.add(it.link);
      out.push(it);
    }
    return out;
  }

  for (const g of groups) {
    g.items = dedupe(g.items);
  }

  const filtered = groups.filter((g) => g.items.length > 0);
  const banner =
    "// AUTO-GENERATED by scripts/summary-to-vitepress-sidebar.mjs — do not edit\n";
  const body = `export default ${JSON.stringify(filtered, null, 2)}\n`;
  fs.mkdirSync(path.dirname(OUT), { recursive: true });
  fs.writeFileSync(OUT, banner + body, "utf8");
  console.error("Wrote", path.relative(ROOT, OUT), "groups:", filtered.length);
}

main();
